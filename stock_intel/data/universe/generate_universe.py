def save_csv(tickers: List[str], filename: str):
    path = os.path.join(UNIVERSE_DIR, filename)
    tickers = sorted({t.strip().upper() for t in tickers if t.strip() and len(t.strip()) <= 5})
    with open(path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["Symbol"])
        for t in tickers:
            writer.writerow([t])
    logger.info(f"Wrote {len(tickers)} tickers to {filename}")

def load_csv(filename: str) -> List[str]:
    path = os.path.join(UNIVERSE_DIR, filename)
    if not os.path.exists(path):
        return []
    with open(path) as f:
        reader = csv.reader(f)
        next(reader, None)
        return [row[0].strip().upper() for row in reader if row and row[0].strip()]

def main():
    save_csv(FALLBACK_SP500, "sp500.csv")
    save_csv(FALLBACK_NASDAQ100, "nasdaq100.csv")
    save_csv(FALLBACK_RUSSELL2000, "russell2000.csv")

    all_tickers = set()
    for fn in ("sp500.csv", "nasdaq100.csv", "russell2000.csv"):
        all_tickers.update(load_csv(fn))

    logger.info(f"Total universe: {len(all_tickers)} unique tickers ({len(FALLBACK_SP500)} SP500 + {len(FALLBACK_NASDAQ100)} NASDAQ100 + {len(FALLBACK_RUSSELL2000)} Russell2000)")

if __name__ == "__main__":
    main()
